let n1 = 90
let n2 = 45

//Compara se um é igual ao outro
console.log(n1 === n2) //FALSE

//Compara se um é diferente do outro
console.log(n1 !== n2) //TRUE

// Compara se um é maior que o outro
console.log(n1 > n2) // TRUE

//Compara se um é MAIOR ou IGUAL ao outro
console.log(n1 >= n2) // TRUE

 //Compara se um é menor que o outro
 console.log(n1 < n2) //FALSE

 //Compara se um é MENOR ou IGUAL ao outro
 console.log(n1 <= n2) //FALSE